    public ResultSet find(String s) throws SQLException{
        try{
            //3568178
            
            String sql ="select * from admin where admin_id=?";
             Class.forName("com.mysql.jdbc.Driver");
             
            con=DriverManager.getConnection("jdbc:mysql:library.sql");
           
            ps = con.prepareStatement(sql);
            ps.setString(1,s);
            rs = ps.executeQuery();
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(StudentProfile.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        return rs;
        
    }
    
    public void run(String AdNum){
        
        ResultSet rs = null;
        try {
            
            rs = find(AdNum);
           
            if(rs.next()){
                
                nameField.setText(rs.getString("admin_name"));
                surnameField.setText(rs.getString("admin_surname"));
       
                emailField.setText(rs.getString("admin_email"));
                contactNumberField.setText(rs.getString("admin_contact"));
                imageOption.setIcon(ResizeImage(null,rs.getBytes("image")));
              
            }
            else{
                 JOptionPane.showMessageDialog(this,"Am confused now");
            }
                
        } catch (SQLException ex) {
            Logger.getLogger(StudentProfile.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
