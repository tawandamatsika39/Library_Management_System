    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        if(checkInputs()&& (ImgPath!=null)){
            Connection co=connectTo();
            try {
                PreparedStatement ps=co.prepareStatement("INSERT INTO admin(admin_id,admin_name,admin_surname,admin_email,admin_contact,admin_password,image) values(?,?,?,?,?,?,?)");
                ps.setString(1,txt_id.getText());
                ps.setString(2,txt_name.getText());
                ps.setString(3,txt_surname.getText());
                ps.setString(4,txt_email.getText());
                ps.setString(5,txt_contact.getText());
                ps.setString(6,txt_password.getText());
                //ps.setString(7,txt_password.getText());
                InputStream img=new FileInputStream(new File(ImgPath));
                ps.setBlob(7,img);

                ps.executeUpdate();
                loadJTable();
                JOptionPane.showMessageDialog(null,"Admin added");
            } catch (SQLException | FileNotFoundException | HeadlessException ex) {
                JOptionPane.showMessageDialog(null,ex.getMessage());
            }
        }
        else{
            JOptionPane.showMessageDialog(null,"One or more nonnull fields are empty!");
        }
    }//GEN-LAST:event_btn_addActionPerformed

