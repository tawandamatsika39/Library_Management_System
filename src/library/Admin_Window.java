    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if(!(txt_id.getText()).equals("")){

            try {
                Connection conn=connectTo();
                PreparedStatement ps=conn.prepareStatement("DELETE FROM admin WHERE admin_id=?");
                String number=txt_id.getText();
                ps.setString(1,number);
                ps.executeUpdate();
                loadJTable();
                JOptionPane.showMessageDialog(null,"Student Deleted");
            } catch (SQLException ex) {
                Logger.getLogger(Admin_Window.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null,"Student Not Deleted");
            }
        }else{
            JOptionPane.showMessageDialog(null,"Student Not Deleted:Student number must be entered to delete");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

