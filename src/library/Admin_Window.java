          public ResultSet searchOperation(String str){
        Connection co=connectTo();
        ResultSet rs=null;
        try {
            PreparedStatement ps=co.prepareStatement("SELECT * FROM admin WHERE admin_id=? ");
            ps.setString(1,str);
            rs=ps.executeQuery();
        } catch (SQLException ex) {
            //Logger.getLogger(Student_Window.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null,ex.getMessage());
        }
        return rs;
    }
