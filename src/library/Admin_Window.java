    private void loadJTable(){
        ArrayList<Admin> list=getAdminList();
        DefaultTableModel model=(DefaultTableModel)JTable_Admin.getModel();
        model.setRowCount(0);
        Object [] row=new Object[7];
        
        for(int i=0;i<list.size();i++){
            row[0]=list.get(i).getId();
            row[1]=list.get(i).getName();
            row[2]=list.get(i).getSurname();
            row[3]=list.get(i).getEmail();
            row[4]=list.get(i).getContact();
            row[5]=list.get(i).getPassword();
            //row[]=list.get(i).getPic();
            row[6]=list.get(i).getPic();
            
            model.addRow(row);
        }
    }     public Connection connectTo()
    {
         Connection conn=null;
        try 
        {
            conn=DriverManager.getConnection("jdbc:mysql:library.sql");
            //JOptionPane.showMessageDialog(null,"CONNECTED!!");
            return conn;
        } catch (SQLException ex) 
        {
            Logger.getLogger(Admin_Window.class.getName()).log(Level.SEVERE, null, ex);
            //JOptionPane.showMessageDialog(null,"NOT CONNECTED");
            return null;
        }
    }
     public ArrayList<Admin> getAdminList(){
        ArrayList<Admin> adminList=new ArrayList<Admin>();
        Connection conn=connectTo();
        String query="SELECT * FROM admin";
            
        Statement st;
        ResultSet rs;
            
        try {
            
            st=conn.createStatement();
            rs=st.executeQuery(query);
            
            Admin admin;
            while(rs.next()){
                
                admin=new Admin(rs.getString("admin_id"),rs.getString("admin_name"),rs.getString("admin_surname"),rs.getString("admin_email"),rs.getString("admin_contact"),rs.getString("admin_password"),rs.getBytes("image"));
                adminList.add(admin);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Admin_Window.class.getName()).log(Level.SEVERE, null, ex);
        }
        return adminList;
    }
