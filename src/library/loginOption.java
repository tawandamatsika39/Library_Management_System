    private void StButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_StButtonActionPerformed
       
        String sql ="select * from student where student_number=? and student_password=?";
        Connection connection;
       PreparedStatement ps;
       
       try{
           Class.forName("com.mysql.jdbc.Driver");
           connection = (Connection) DriverManager.getConnection("jdbc:mysql:library.sql");
           
           ps = (PreparedStatement) connection.prepareStatement(sql);
           ps.setString(1,stu_number.getText());
           ps.setString(2,String.valueOf(stud_pass.getPassword()));
           ResultSet result = ps.executeQuery();
           if(result.next()){
               String sn = stu_number.getText();
               new StudentProfile(sn).setVisible(true);
               close();
           }
           else{
               JOptionPane.showMessageDialog(null,"This student does not Exist");
           }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(loginOption.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_StButtonActionPerformed
    
    
