    public static ArrayList<Student> getStudentList(){
        ArrayList<Student> studentList=new ArrayList<Student>();
        Connection conn=connectTo();
        String query="SELECT * FROM student";
            
        Statement st;
        ResultSet rs;
            
        try {
            
            st=conn.createStatement();
            rs=st.executeQuery(query);
            
            Student student;
            while(rs.next()){
                
                student=new Student(rs.getString("student_number"),rs.getString("student_name"),rs.getString("student_program"),rs.getString("student_email"),rs.getString("book1barcode"),rs.getString("book2barcode"),rs.getString("book3barcode"),rs.getBytes("image"),rs.getString("student_password"));
                studentList.add(student);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Student_Window.class.getName()).log(Level.SEVERE, null, ex);
        }
        return studentList;
    }
    
