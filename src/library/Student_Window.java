    public static void loadJTable(){
        ArrayList<Student> list=getStudentList();
        DefaultTableModel model=(DefaultTableModel)JTable_Student.getModel();
        model.setRowCount(0);
        Object [] row=new Object[7];
        
        for(int i=0;i<list.size();i++){
            row[0]=list.get(i).getNumber();
            row[1]=list.get(i).getName();
            row[2]=list.get(i).getProgram();
            row[3]=list.get(i).getEmail();
            row[4]=list.get(i).getBook1_barcode();
            row[5]=list.get(i).getBook2_barcode();
            //row[]=list.get(i).getPic();
            row[6]=list.get(i).getBook3_barcode();
            
            model.addRow(row);
        }
    }
