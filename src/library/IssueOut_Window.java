    public ImageIcon ResizeImage(String imagePath,byte[] pic){
        ImageIcon myImage;
        if(imagePath!=null){
            myImage=new ImageIcon(imagePath);
        }
        else{
            myImage=new ImageIcon(pic);
        }  
        Image img= myImage.getImage();
        Image img2=img.getScaledInstance(img_Field.getWidth(),img_Field.getHeight(),Image.SCALE_SMOOTH);
        ImageIcon image=new ImageIcon(img2);
        return image;
    }
    public void setFields(){
        ImageIcon a=(ImageIcon) Book_Window.lbl_img.getIcon();
        Image b=a.getImage().getScaledInstance(img_Field.getWidth(),img_Field.getHeight(),Image.SCALE_SMOOTH);
        barcode_Field.setText(Book_Window.txt_barcode.getText());
        isbn_Field.setText(Book_Window.txt_isbn.getText());
        name_Field.setText(Book_Window.txt_name.getText());
        author_Field.setText(Book_Window.txt_author.getText());
        subject_Field.setText(Book_Window.combo_subject.getSelectedItem().toString());
        edition_Field.setText(Book_Window.txt_edition.getText());
        status_Field.setText(Book_Window.combo_status.getSelectedItem().toString());
        img_Field.setIcon(new ImageIcon(b));
    }
           
