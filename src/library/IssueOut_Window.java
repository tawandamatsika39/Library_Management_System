    public Connection connectTo()
    {
         Connection conn=null;
        try 
        {
            conn=DriverManager.getConnection("jdbc:mysql:library.sql");
            //JOptionPane.showMessageDialog(null,"CONNECTED!!");
            return conn;
        } catch (SQLException ex) 
        {
            Logger.getLogger(IssueOut_Window.class.getName()).log(Level.SEVERE, null, ex);
            //JOptionPane.showMessageDialog(null,"NOT CONNECTED");
            return null;
        }
    }
    
    public boolean checkInputs()
    {
        if(
              number_Field.getText()==null
            && date_Field.getDate()==null)
            {
        return false;
        }
        else{
            return true;
        }
    }
   
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        if(checkInputs()){
            Connection co=connectTo();
            try {
                PreparedStatement ps=co.prepareStatement("INSERT INTO issuedbook(book_barcode,book_isbn,book_name,author,edition,subject,student_number,due_date) values(?,?,?,?,?,?,?,?)");
                ps.setString(1,barcode_Field.getText());
                ps.setString(2,isbn_Field.getText());
                ps.setString(3,name_Field.getText());
                ps.setString(4,author_Field.getText());
                ps.setString(5,edition_Field.getText());
                ps.setString(6,subject_Field.getText());
                ps.setString(7,number_Field.getText());
                ps.setString(8,date_Field.getDate().toString());
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null,"Book Issued Out");
               
                Connection conn=connectTo();
                try {
                 
                    PreparedStatement ps3=conn.prepareStatement("DELETE FROM book WHERE book_barcode=?");
                    String barcode=barcode_Field.getText();
                    ps3.setString(1, barcode);
                    ps3.executeUpdate();
                    Book_Window.loadJTable();
                //JOptionPane.showMessageDialog(null,"Book Deleted");
                } 
                catch (SQLException ex) {
                    Logger.getLogger(Book_Window.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showMessageDialog(null,ex.getMessage());
                }  
                } catch (SQLException | HeadlessException ex) {
                    JOptionPane.showMessageDialog(null,ex.getMessage());  
                } catch (SecurityException | IllegalArgumentException ex) {
                    Logger.getLogger(IssueOut_Window.class.getName()).log(Level.SEVERE, null, ex);
                }
                }
                else{
                    JOptionPane.showMessageDialog(null,"One or more nonnull fields are empty!");
                }
        
    }//GEN-LAST:event_jButton1ActionPerformed

