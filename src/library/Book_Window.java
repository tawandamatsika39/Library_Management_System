    public static ArrayList<Book> getBookList(){
        ArrayList<Book> bookList=new ArrayList<Book>();
        Connection conn=connectTo();
        String query="SELECT * FROM book";
            
        Statement st;
        ResultSet rs;
            
        try {
            
            st=conn.createStatement();
            rs=st.executeQuery(query);
            
            Book book;
            while(rs.next()){
                
                book=new Book(rs.getString("book_barcode"),rs.getString("book_isbn"),rs.getString("book_name"),rs.getString("author"),Integer.parseInt(rs.getString("edition")),rs.getString("subject"),rs.getBytes("image"),rs.getString("book_status"));
                bookList.add(book);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Book_Window.class.getName()).log(Level.SEVERE, null, ex);
        }
        return bookList;
    }
    
    public static void loadJTable(){
        ArrayList<Book> list=getBookList();
        DefaultTableModel model=(DefaultTableModel)JTable_Books.getModel();
        model.setRowCount(0);
        Object [] row=new Object[7];
        
        for(int i=0;i<list.size();i++){
            row[0]=list.get(i).getBarcode();
            row[1]=list.get(i).getIsbn();
            row[2]=list.get(i).getTitle();
            row[3]=list.get(i).getAuthor();
            row[4]=list.get(i).getEdition();
            row[5]=list.get(i).getSubject();
            //row[]=list.get(i).getPic();
            row[6]=list.get(i).getStatus();
            
            model.addRow(row);
        }
    }

